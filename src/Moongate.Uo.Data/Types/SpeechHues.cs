namespace Moongate.Uo.Data.Types;

/// <summary>
/// Common hue colors for speech
/// </summary>
public static class SpeechHues
{
    public const ushort Normal = 0x03B2;    // Normal text color
    public const ushort System = 0x0035;    // System blue
    public const ushort Broadcast = 0x0021; // Bright blue
    public const ushort Error = 0x0026;     // Red
    public const ushort Success = 0x0040;   // Green
    public const ushort Warning = 0x0030;   // Yellow
    public const ushort Emote = 0x0026;     // Light blue
    public const ushort Whisper = 0x0096;   // Gray
    public const ushort Yell = 0x0021;      // Bright color
}
